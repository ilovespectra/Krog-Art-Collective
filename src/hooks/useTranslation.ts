const translations = {
  en: enTranslations,
  sl: slTranslations,
};

export function useTranslation() {
  const { locale } = useLanguage();

  const t = (key: string, defaultValue?: string): string | string[] => {
    const keys = key.split('.');
    let value: Record<string, unknown> | unknown = translations[locale as keyof typeof translations];

    for (const k of keys) {
      if (typeof value === 'object' && value !== null) {
        value = (value as Record<string, unknown>)[k];
      }
    }

    if (Array.isArray(value)) return value;
    return String(value || defaultValue || key);
  };

  return { t, locale };
}
